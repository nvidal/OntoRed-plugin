package uy.edu.fing.ontored.exceptions;

public class EliminarException extends Exception {

	private static final long serialVersionUID = 1L;

	public EliminarException() {
		super();
	}

	public EliminarException(String message, Throwable cause) {
		super(message, cause);
	}

	public EliminarException(String message) {
		super(message);
	}

	public EliminarException(Throwable cause) {
		super(cause);
	}
	
}
